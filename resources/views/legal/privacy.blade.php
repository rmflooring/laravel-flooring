    <p>This Privacy Policy describes how RM Flooring ("Company", "we", "us") collects, uses, and protects information in connection with the Floor Manager application ("Software"), including its QuickBooks Online ("QBO") integration.</p>

    <h2>1. Scope</h2>
    <p>This policy applies to all users of the Floor Manager Software, which is an internal business application used exclusively by RM Flooring staff to manage jobs, estimates, sales, invoices, and customer records.</p>

    <h2>2. Information We Collect</h2>
    <p>We collect and store the following categories of information within the Software:</p>
    <ul>
        <li><strong>Customer data:</strong> Names, addresses, phone numbers, and email addresses of RM Flooring customers.</li>
        <li><strong>Job and financial data:</strong> Estimates, sale records, invoice amounts, line items, and payment information related to flooring projects.</li>
        <li><strong>Staff account data:</strong> Employee names and login credentials for Software access.</li>
        <li><strong>QuickBooks Online data:</strong> Financial records synchronized with the Company's authorized QBO account, including customer records and invoice data.</li>
        <li><strong>Usage data:</strong> Application logs and activity records for operational and security purposes.</li>
    </ul>

    <h2>3. How We Use Information</h2>
    <p>Information collected within the Software is used solely to:</p>
    <ul>
        <li>Manage RM Flooring's internal business operations (job tracking, estimating, invoicing).</li>
        <li>Synchronize financial data with QuickBooks Online for accounting and reporting purposes.</li>
        <li>Support staff workflows and internal communications.</li>
        <li>Maintain the security and integrity of the Software.</li>
    </ul>
    <p>We do not sell, rent, or share customer or staff data with any third party for marketing purposes.</p>

    <h2>4. QuickBooks Online Integration</h2>
    <p>The Software connects to Intuit's QuickBooks Online platform via the official Intuit API. When the QBO integration is used:</p>
    <ul>
        <li>Customer and invoice data may be transmitted to and from the Company's authorized QBO account.</li>
        <li>Only data necessary for accounting reconciliation is exchanged.</li>
        <li>No data is shared with Intuit beyond what is required for the API integration.</li>
        <li>Intuit's own <a href="https://www.intuit.com/privacy/statement/" target="_blank" rel="noopener">Privacy Statement</a> governs data held within QuickBooks Online.</li>
    </ul>

    <h2>5. Data Storage and Security</h2>
    <p>All Software data is stored on RM Flooring's own servers located in Canada. We implement reasonable technical and organizational safeguards, including access controls and encrypted connections (HTTPS), to protect data against unauthorized access, loss, or disclosure.</p>

    <h2>6. Data Retention</h2>
    <p>We retain customer and job data for as long as necessary to fulfill business, legal, and accounting obligations. Staff account data is removed upon termination of employment.</p>

    <h2>7. Access and Disclosure</h2>
    <p>Access to the Software is restricted to authorized RM Flooring staff. We do not disclose personal information to third parties except:</p>
    <ul>
        <li>As required by applicable law or legal process.</li>
        <li>To the extent necessary to operate the QBO integration as described in Section 4.</li>
    </ul>

